
import java.util.Scanner;

public class InputOutput {

	
	public String getString() {
		
		Scanner sc = new Scanner(System.in);
		
		String res = sc.nextLine();
		
		return res;
		
		
	}
	
	public int getInt() {
		
		Scanner sc = new Scanner(System.in);
		
		int res = sc.nextInt();
		
		return res;
	}
	
	
	
	
}
